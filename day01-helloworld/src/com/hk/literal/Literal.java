public class Literal {
    public static void main(String[] args) {
        // 目标：掌握常见数据在程序中的书写格式
        // 1. 整数
        System.out.println(666);
        // 2. 小数
        System.out.println(11.1);
        // 3. 字符
        System.out.println('a');
        // 4. 字符串
        System.out.println("中国");
        // 5. 特殊的字符：\n表示换行 \t代表一个tab
    }
}
